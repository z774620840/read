package org.igeek.pojo;

public class ViewClientUsers {
	private Integer user_Id;
	private String username;
	private Integer user_Type;
	private Integer userInfo_Id;
	private Integer client_Id;
	private String client_Name;
	private String client_Company;
	private String client_Email;

	public Integer getUser_Id() {
		return user_Id;
	}

	public void setUser_Id(Integer user_Id) {
		this.user_Id = user_Id;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public Integer getUser_Type() {
		return user_Type;
	}

	public void setUser_Type(Integer user_Type) {
		this.user_Type = user_Type;
	}

	public Integer getUserInfo_Id() {
		return userInfo_Id;
	}

	public void setUserInfo_Id(Integer userInfo_Id) {
		this.userInfo_Id = userInfo_Id;
	}

	public Integer getClient_Id() {
		return client_Id;
	}

	public void setClient_Id(Integer client_Id) {
		this.client_Id = client_Id;
	}

	public String getClient_Name() {
		return client_Name;
	}

	public void setClient_Name(String client_Name) {
		this.client_Name = client_Name;
	}

	public String getClient_Company() {
		return client_Company;
	}

	public void setClient_Company(String client_Company) {
		this.client_Company = client_Company;
	}

	public String getClient_Email() {
		return client_Email;
	}

	public void setClient_Email(String client_Email) {
		this.client_Email = client_Email;
	}

}
